let numberX = 10;
let numberY = '20';
console.log(numberX == numberY); // false
console.log(numberX === numberY); // false
console.log(numberX != numberY); // true
console.log(numberX !== numberY); // true
console.log(numberX > numberY); // false
console.log(numberX >= numberY); // false
console.log(numberX < numberY); // true
console.log(numberX <= numberY); // true
console.log((numberX != numberY)?'It is true':'It is false'); // It is true